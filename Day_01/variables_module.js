let message;
//This creates a container named message which can store data.

message = 'Hello, World!!';
//Here we have assigned a string to the variable using an assignment operator '='.

alert(message);
//This alerts the content inside the variable 'message'.


let message_2 = 'Hello, People';
//We can also assign the variable while declaring the variable too.

alert(message_2);
//This alert works the same as above. 

message = 'This is changed';
//We can also change the value of the variable, now when we alert it, it will show the new value, the old value gets deleted.

alert(message);

